class OrderStatuses {
  // =========================
  // حالات طلب المورد
  // =========================
  static const List<String> supplier = [
    'قيد الإنشاء',
    'تم الإنشاء',
    'في انتظار الدمج',
    'تم دمجه مع العميل',
    'جاهز للتحميل',
    'تم التحميل',
    'في الطريق',
    'تم التسليم',
    'ملغى',
  ];

  // =========================
  // حالات طلب العميل
  // =========================
  static const List<String> customer = [
    'في انتظار التخصيص',
    'تم تخصيص طلب المورد',
    'تم دمجه مع المورد',
    'في انتظار التحميل',
    'في الطريق',
    'تم التسليم',
  ];

  // =========================
  // حالات الطلب المدمج
  // =========================
  static const List<String> merged = [
    'تم الدمج',
    'مخصص للعميل',
    'جاهز للتحميل',
    'تم التحميل',
    'في الطريق',
    'تم التسليم',
    'تم التنفيذ',
  ];

  // =========================
  // الحالات النهائية
  // =========================
  static const List<String> finalStatuses = [
    'تم التسليم',
    'تم التنفيذ',
    'مكتمل',
    'ملغى',
  ];

  // =========================
  // helper حسب نوع الطلب
  // =========================
  static List<String> bySource(String source) {
    switch (source) {
      case 'مورد':
        return supplier;
      case 'عميل':
        return customer;
      case 'مدمج':
        return merged;
      default:
        return [];
    }
  }

  static bool isFinal(String status) {
    return finalStatuses.contains(status);
  }
}
